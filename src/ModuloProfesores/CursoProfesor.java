    public void actualizarListadoAlumnos(){
        if (contadorAlumnos > 0) {

            // actualizar listado
            DefaultTableModel modeloListado = (DefaultTableModel) ListadoAlumnos.getModel();
            modeloListado.setRowCount(contadorAlumnos);

            for (int i = 0; i < contadorAlumnos; i++) {
                modeloListado.setValueAt(Alumnos[i].codigo, i, 0);
                modeloListado.setValueAt(Alumnos[i].nombre, i, 1);
                modeloListado.setValueAt(Alumnos[i].apellido, i, 2);
               
            }
        }
    }
    
    public void actualizarActividades(){
        if (contadorActividades>0){

            // actualizar listado
            DefaultTableModel modeloListado = (DefaultTableModel) tablaActividades.getModel();
            modeloListado.setRowCount(contadorActividades);

            for (int i = 0; i < contadorActividades; i++) {
                modeloListado.setValueAt(Actividades[i].nombreActividad, i, 0);
                modeloListado.setValueAt(Actividades[i].descripcionActividad, i, 1);
                modeloListado.setValueAt(Actividades[i].ponderacionActividad, i, 2);
               
            }
        }
    }
    
