    private void crearActividadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearActividadActionPerformed
        // TODO add your handling code here:
        
        String nombre = nombreIn.getText();
        String descripcion = descripcionIn.getText();
        String ponderacion = ponderacionIn.getText();
        
        int i = Integer.parseInt(ponderacion);
        
        if(i+acumulado<=100){
            Actividad nuevaActividad = new Actividad(nombre, descripcion, ponderacion);
        
            Actividades[contadorActividades]=nuevaActividad;
        
            contadorActividades++;
        
            actualizarActividades();
        
            nombreIn.setText("");
            descripcionIn.setText("");
            ponderacionIn.setText("");
            
            acumulado+=i;
            
            mostrarAcumulado.setText(acumulado + "/100");
        }else{
            JOptionPane.showMessageDialog(null, "El acumulado no puede ser mayor que 100", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_crearActividadActionPerformed

